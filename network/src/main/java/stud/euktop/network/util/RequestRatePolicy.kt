package stud.euktop.network.util

enum class RequestRatePolicy {
    /**
     * Без ограничений: каждый вызов идёт в сеть.
     */
    NONE,

    /**
     * Debounce: важен последний параметр.
     * Запрос отправляется только после паузы (N мс) без новых вызовов.
     */
    DEBOUNCE,

    /**
     * Throttle: не чаще, чем раз в N мс.
     * Промежуточные вызовы игнорируются или откладываются.
     */
    THROTTLE,

    /**
     * Batch/Dedup: объединяем несколько одинаковых запросов
     * в один реальный, результат рассылаем всем ожидающим.
     */
    BATCH
}